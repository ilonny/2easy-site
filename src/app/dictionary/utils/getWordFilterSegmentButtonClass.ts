import {
  DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_ACTIVE_CLASS,
  DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_BASE_CLASS,
  DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_INACTIVE_CLASS,
  DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_LEFT_CLASS,
  DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_RIGHT_CLASS,
} from "../constants";

export const getWordFilterSegmentButtonClass = (
  isActive: boolean,
  side: "left" | "right"
) =>
  [
    DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_BASE_CLASS,
    side === "left"
      ? DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_LEFT_CLASS
      : DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_RIGHT_CLASS,
    isActive
      ? DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_ACTIVE_CLASS
      : DICTIONARY_WORD_FILTER_SEGMENT_BUTTON_INACTIVE_CLASS,
  ].join(" ");
