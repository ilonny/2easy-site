function shuffleArray(arr: Array<any>) {
  const shuffled = JSON.parse(JSON.stringify(arr));

  for (let i = 0; i < arr.length; i++) {
    const num = Math.floor(Math.random() * arr.length);
    const temp = shuffled[i];
    shuffled[i] = shuffled[num];
    shuffled[num] = temp;
  }
  return shuffled;
}

